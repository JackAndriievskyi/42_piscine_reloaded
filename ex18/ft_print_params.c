void	ft_putchar();

int		main(int ar, char **av)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	while (++i < ar)
	{
		j = -1;
		while ((c = av[i][++j]) != '\0')
			ft_putchar(c);
		ft_putchar('\n');
	}
	return (0);
}
